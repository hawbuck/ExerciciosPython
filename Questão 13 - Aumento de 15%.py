#Faça um algoritmo que leia o salário de uma funcionário e mostre seu novo salário, com 15% de aumento

#Recebendo o salário
salario = float(input('Digite o seu salário: '))

#Calculando o novo salário com 15% de aumento
novoSalario = salario * 1.15

#Mostrando o novo salário
print('Seu novo salário é de {}'.format(novoSalario))